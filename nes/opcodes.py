"""
This module contains the opcode definitions for the 6502 processor.
"""
import enum
from numpy import uint8

class Opcodes(enum.Enum):
    """
    Enumeration representing the opcodes used in the NES emulator.
    Each opcode is assigned a unique uint8 value.
    """

    BRK = uint8(0)
    ORA = uint8(1)
    ASL = uint8(2)
    BPL = uint8(3)
    CLC = uint8(4)
    JSR = uint8(5)
    AND = uint8(6)
    ROL = uint8(7)
    BIT = uint8(8)
    PLP = uint8(9)
    BMI = uint8(10)
    SEC = uint8(11)
    RTI = uint8(12)
    EOR = uint8(13)
    LSR = uint8(14)
    PHA = uint8(15)
    JMP = uint8(16)
    BVC = uint8(17)
    CLI = uint8(18)
    RTS = uint8(19)
    ADC = uint8(20)
    ROR = uint8(21)
    STA = uint8(22)
    STX = uint8(23)
    STY = uint8(24)
    DEY = uint8(25)
    TXA = uint8(26)
    BCC = uint8(27)
    TYA = uint8(28)
    TXS = uint8(29)
    LDY = uint8(30)
    TAY = uint8(31)
    BCS = uint8(32)
    LDA = uint8(33)
    LDX = uint8(34)
    TAX = uint8(35)
    CLV = uint8(36)
    TSX = uint8(37)
    CPY = uint8(38)
    INY = uint8(39)
    CMP = uint8(40)
    DEC = uint8(41)
    DEX = uint8(42)
    PLA = uint8(43)
    BNE = uint8(44)
    CLD = uint8(45)
    CPX = uint8(46)
    SBC = uint8(47)
    INC = uint8(48)
    INX = uint8(49)
    NOP = uint8(50)
    BEQ = uint8(51)
    SED = uint8(52)
    BVS = uint8(53)
    SEI = uint8(54)
    PHP = uint8(55)
    JAM = uint8(255)
